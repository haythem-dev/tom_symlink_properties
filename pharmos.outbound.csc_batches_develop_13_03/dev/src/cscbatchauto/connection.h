#ifndef GUARD_CSCBATCHES_CONNECTION_H
#define GUARD_CSCBATCHES_CONNECTION_H

//**************************************************************************************************
// includes
//**************************************************************************************************
#include <libbasardbaspect.h>
#include <libbasardbsql_databaseinfo.h>

//**************************************************************************************************
// usings
//**************************************************************************************************
using basar::db::aspect::ConnectionRef;

namespace cscbatchauto {
	//**************************************************************************************************
	// class declaration
	//**************************************************************************************************
	//-------------------------------------------------------------------------------------------------//
	/*! \brief connection class
		This is a final class.  
		\throw BasarExceptions
	*/
	//-------------------------------------------------------------------------------------------------//
	class Connection {
	public:	
		//**************************************************************************************************
		/*! \brief Std constructor
			\throw no-throw 
		*/
		//**************************************************************************************************
		Connection();

		//**************************************************************************************************
		/*! \brief Std destructor
			\throw no-throw 
		*/
		//**************************************************************************************************
		virtual ~Connection();
		
		//**************************************************************************************************
		/*! \brief Connects to database.
			\throw no-throw 
		*/
		//**************************************************************************************************
		bool connect(basar::I18nString Server, basar::I18nString Db);
		
		//**************************************************************************************************
		/*! \brief Disconnect the open connection.
			\throw no-throw 
		*/
		//**************************************************************************************************
		void disconnect();

		//**************************************************************************************************
		/*! \brief Gets the current CSC connection.
			\return Reference to current CSC connection; used to create an instance of an accessor definition
			\throw no-throw 
		*/
		//**************************************************************************************************
		const ConnectionRef getConnectionCsc() const;

	private:
		//**************************************************************************************************
		/*! \brief Copy constuctor.
			\note forbidden
		*/
		//**************************************************************************************************
		Connection(const Connection & r);

		//**************************************************************************************************
		/*! \brief Assignment constuctor.
			\note forbidden
		*/
		//**************************************************************************************************
		Connection & operator =	(const Connection &r);
		
		basar::db::sql::DatabaseInfo	m_DbInfo;		//!< info to active infx connection
		ConnectionRef m_ActivConn;	//!< reference to active infx connection
	};
}// namespace
#endif	// GUARD
